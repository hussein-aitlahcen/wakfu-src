    public static class LanguageView extends ImmutableFieldProvider
    {
        public static final String TEXT = "text";
        public static final String ICON_URL = "iconUrl";
        private Language m_language;
        
        private LanguageView(final Language language) {
            super();
            this.m_language = language;
        }
        
        @Override
        public String[] getFields() {
            return null;
        }
        
        @Override
        public Object getFieldValue(final String fieldName) {
            if (fieldName.equals("iconUrl")) {
                return WakfuTranslator.getLanguageIconUrl(this.m_language);
            }
            if (fieldName.equals("text")) {
                return this.m_language.getActualLocale().getLanguage().toUpperCase();
            }
            return null;
        }
        
        public Language getLanguage() {
            return this.m_language;
        }
    }
