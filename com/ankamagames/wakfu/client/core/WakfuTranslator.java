    @Override
    public void setLanguage(final Language language) {
        super.setLanguage(language);
        SmsAndFloodController.setLanguage(language);
        WordsModerator.getInstance().setLanguages(CensoredLanguageEnum.getByLanguage(language));
        if (!language.isUsesStandardCharacters()) {
            FontFactory.setEnableAWTFontMode((byte)2);
        }
        TextRenderer.FORCE_CUT_ON_MISSING_SPACE = !language.separatesWords();
        NameValidatorHelper.initialize(language);
        CharacterPacker.setLanguage(language);
    }
    
    public void setCensoredLanguages(final ArrayList<CensoredLanguageEnum> languages) {
        final CensoredLanguageEnum censoredLanguageEnum = CensoredLanguageEnum.getByLanguage(this.getLanguage());
        if (!languages.contains(censoredLanguageEnum)) {
            languages.add(censoredLanguageEnum);
        }
        WordsModerator.getInstance().setLanguages(languages);
    }
    
