    public static String convertImageTags(String s, final int lineHeight) {
        final Matcher matcher = WakfuTranslator.ICON_PATTERN.matcher(s);
        while (matcher.find()) {
            final int gfxId = Integer.parseInt(matcher.group(1));
            final String iconPath = new TextWidgetFormater().addImage(WakfuConfiguration.getInstance().getIconUrl("textIconsPath", "defaultIconPath", gfxId), lineHeight, lineHeight, null).finishAndToString();
            s = StringUtils.replace(s, matcher.group(), iconPath);
        }
        return s;
    }
    
    public static String convertImageTags(final String s) {
        return convertImageTags(s, -1);
    }
    
